#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*tmp;
	size_t	i;

	tmp = (char*)s;
	i = 0;
	while (i < n)
	{
		if (tmp[i] == c)
			return ((void*)(s + i));
		i++;
	}
	return (NULL);
}
